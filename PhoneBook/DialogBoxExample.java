import javax.swing.JOptionPane;

public class DialogBoxExample {
    public static void main(String[] args) {
        // Display an information dialog box
        JOptionPane.showMessageDialog(null, "Welcome to the Dialog Box Example!", "Information", JOptionPane.INFORMATION_MESSAGE);

        // Display a warning dialog box
        JOptionPane.showMessageDialog(null, "This is a warning message.", "Warning", JOptionPane.WARNING_MESSAGE);

        

    }
}
